
def add(number1,number2):
    return number1+number2      #adding the numbers

def subtract(number1,number2):
    return number1-number2   #subtracting the numbers

def multiply(number1,number2):
    return number1*number2      #multiplying the numbers

def divide(number1,number2):
    return number1/number2      #dividing the numbers

def power(number1,number2):
    return number1**number2     #getting the power of number1 to number2

